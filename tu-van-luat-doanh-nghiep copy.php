              định của Luật Trọng tài thương mại 2010, tranh chấp chỉ có thể được giải quyết bằng trọng tài khi có
              thỏa thuận trọng tài và thỏa thuận này không bị vô hiệu. Thỏa thuận trọng tài có thể được lập trước
              hoặc
              sau khi có tranh chấp, bằng văn bản hoặc là một điều khoản trong hợp đồng.</span></p>
