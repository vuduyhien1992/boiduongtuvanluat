              quy định của khoản 2 Điều 24 Luật Doanh nghiệp 2020, nguyên tắc giải quyết tranh chấp nội bộ được chi
              tiết trong Điều lệ của công ty. Do đó, khi xảy ra tranh chấp, quá trình giải quyết thường được tiến
              hành
              theo nguyên tắc đã quy định trong Điều lệ.</span></p>
          <p style="margin-top:6pt; margin-bottom:6pt; line-height:normal; background-color:#ffffff"><span>Thường,
              việc giải quyết tranh chấp nội bộ trong doanh nghiệp đầu tiên được thực hiện thông qua phương thức
              thương lượng và hòa giải. Trong trường hợp không thể đạt được thương lượng hoặc hòa giải, bất kỳ bên
              nào
              cũng có quyền đưa vụ tranh chấp ra cơ quan có thẩm quyền để giải quyết theo quy định của pháp
              luật.</span></p>
          <p
            style="margin-top:6pt; margin-bottom:6pt; line-height:normal; background-color:#ffffff; -aw-outline-level:3">
