          </p>
          <p style="margin-top:6pt; margin-bottom:6pt; line-height:normal; background-color:#ffffff"><span>Trong
              việc
              giải quyết tranh chấp nội bộ doanh nghiệp, pháp luật thường khuyến khích các bên tự thương lượng và
              hòa
              giải với nhau. Phương thức này giúp hài hòa lợi ích của cả hai bên một cách hiệu quả và tiết kiệm chi
              phí.</span></p>
          <p style="margin-top:6pt; margin-bottom:6pt; line-height:normal; background-color:#ffffff"><span
              style="font-weight:bold">2.2 Hoà giải thương mại</span></p>
          <p style="margin-top:6pt; margin-bottom:6pt; line-height:normal; background-color:#ffffff"><span>Theo quy
              định của khoản 1 Điều 3 Nghị định 22/2017/NĐ-CP, hòa giải thương mại là phương thức giải quyết tranh
              chấp thương mại do các bên thỏa thuận, với sự hỗ trợ của hòa giải viên thương mại làm trung gian. Quy
              trình hòa giải thương mại được thực hiện theo Điều 14 Nghị định 22/2017/NĐ-CP.</span></p>
          <p style="margin-top:6pt; margin-bottom:6pt; line-height:normal; background-color:#ffffff"><span>Các bên
              có
              quyền lựa chọn quy tắc hòa giải của tổ chức hòa giải thương mại hoặc tự thỏa thuận trình tự, thủ tục
              hòa
              giải. Trong trường hợp không đạt được thỏa thuận, hòa giải viên thương mại sẽ áp dụng trình tự, thủ
              tục
              phù hợp với tình tiết và nguyện vọng của các bên, sau khi được các bên chấp thuận.</span></p>
